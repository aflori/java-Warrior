package Tools.Defense;

public abstract class DefenseTool {
    protected String name;

    public DefenseTool(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return this.name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

